async function updatePost(id: number, content: string, title: string) {
  const result = await prisma.posts.update({
    where: { id },
    data: {
      content,
      updated_at: new Date()
    }
  });
  console.log(`Updated post ${result.id}: ${title}`);
  return result;
}

